/**
 * y = f(x)
 */
public class Data {
    double x;
    double y;

    public Data(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public Data() {
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
